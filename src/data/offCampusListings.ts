// Helper function to convert listings to a text format for LLM
export function listingsToText(listings: Listing[]): string {
  return listings.map((l, i) => 
    `${i + 1}. "${l.title}" - $${l.price}/month
   Address: ${l.address}
   Zone: ${l.zone}
   Type: ${l.propertyType} | Bedrooms: ${l.bedrooms === 0 ? 'Studio' : l.bedrooms}
   Available: ${l.availableFrom}
   Amenities: ${l.amenities.join(', ')}
   Description: ${l.description}`
  ).join('\n\n');
}
